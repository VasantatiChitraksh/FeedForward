import 'package:flutter/material.dart';

class AppTheme {
  static const Color primaryColor = Color.fromARGB(220, 194, 165, 118);
  static const Color secondaryColor = Color.fromARGB(255, 225, 119, 102);

  static Color bgcolor() {
    return primaryColor;
  }

  static Color titleColor() {
    return secondaryColor;
  }
}
